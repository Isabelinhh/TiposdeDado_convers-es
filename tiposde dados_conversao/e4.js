// 1. Converter boolean para número
let booleanValue = true;
let booleanToNumber = Number(booleanValue);

console.log("Boolean convertido:", booleanToNumber);
console.log("Tipo:", typeof booleanToNumber);

// 2. Converter null para número
let nullValue = null;
let nullToNumber = Number(nullValue);

console.log("Null convertido:", nullToNumber);
console.log("Tipo:", typeof nullToNumber);

// 3. Converter undefined para número
let undefinedValue = undefined;
let undefinedToNumber = Number(undefinedValue);

console.log("Undefined convertido:", undefinedToNumber);
console.log("Tipo:", typeof undefinedToNumber);
